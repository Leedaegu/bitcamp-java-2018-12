 //문자 제어코드
package ch02;

public class Test10 {

  public static void main(String[] args) {     
    System.out.println("Hello,\nworld!"); //줄바꿈은 이 제어문자를 사용하라!
    System.out.println("Hello,\rworld!");
    System.out.println("Hello,\b\b\bworld!");
    System.out.println("Hello,\tworkd!");
    System.out.println("Hello,\fworkd!");
    System.out.println("Hello,\"w\"orkd!");
    System.out.println("Hello,'w'orkd!");
    System.out.println('\'');
    System.out.println('"');
    System.out.println("Hello,\\workd!");
   
    
    
  }
}

